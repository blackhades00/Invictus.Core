        internal static void DrawCircleRange(Vector3 position, float radius, Color color, float thickness,
            bool filled = false)
        {
            if (_effect == null || _effect.IsDisposed) return;

            var vertexDeclaration = Device.VertexDeclaration;
            _effect.Begin();
            Device.SetStreamSource(0, _vertexBuffer, 0, Utilities.SizeOf<Vector4>());
            Device.VertexDeclaration = _vertexDeclaration;
            var viewMatrix = Renderer.GetViewMatrix();
            var projectionMatrix = Renderer.GetProjectionMatrix();
            var value = position;
            _effect.BeginPass(0);
            _effect.SetValue("ProjectionMatrix", Matrix.Translation(value) * viewMatrix * projectionMatrix);
            _effect.SetValue("Color",
                new Vector4((float)color.R / 255f, (float)color.G / 255f, (float)color.B / 255f,
                    (float)color.A / 255f));
            _effect.SetValue("Radius", radius);
            _effect.SetValue("Width", thickness);
            _effect.SetValue("Filled", filled);
            _effect.SetValue("EnableZ", false);
            _effect.SetValue("antiAlias", 1.25f);
            _effect.EndPass();
            Device.DrawPrimitives(PrimitiveType.TriangleList, 0, 1);
            _effect.CommitChanges();
            _effect.End();
            Device.VertexDeclaration = vertexDeclaration;
        }

