        // Method for drawing Circle
        internal static void DrawCircle(int x, int y, float radius, int numSides, Color color)
        {
            var line = new Vector2[100];

            var step = (float)(Math.PI * 2.0 / numSides);
            var count = 0;
            for (float a = 0; a < Math.PI * 2.0; a += step)
            {
                var x1 = (float)(radius * Math.Cos(a) + x);
                var y1 = (float)(radius * Math.Sin(a) + y);
                var x2 = (float)(radius * Math.Cos(a + step) + x);
                var y2 = (float)(radius * Math.Sin(a + step) + y);

                line[count].X = x1;
                line[count].Y = y1;
                line[count + 1].X = x2;
                line[count + 1].Y = y2;
                count += 2;
            }

            DrawCircleLine.Begin();
            DrawCircleLine.Draw(line, color);
            DrawCircleLine.End();
        }

        internal static void DrawEllipse(int x, int y, float radiusX, float radiusY, float width, Color color)
        {
            var line = new Vector2[360];

            int i;

            for (i = 0; i < 360; i++)
            {
                var rad = i * 3.14159 / 180.0;
                line[i].X = x + (float)Math.Cos(rad) * radiusX;
                line[i].Y = y + (float)Math.Sin(rad) * radiusY;
            }

            DrawCircleLine.Width = width;
            DrawCircleLine.Begin();
            DrawCircleLine.Draw(line, color);
            DrawCircleLine.End();
        }

        internal static void DrawFilledCircle(int x, int y, float radius, int numsides, Color color)
        {
            for (float i = 0; i <= radius; i += 1.5f)
            {
                DrawCircle(x, y, i + 0.5f, numsides, color);
                DrawCircle(x, y, i + 1.0f, numsides, color);
                DrawCircle(x, y, i + 1.5f, numsides, color);
            }
        }

