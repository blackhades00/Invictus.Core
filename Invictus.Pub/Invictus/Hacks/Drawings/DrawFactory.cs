        internal static void DrawFont(string text, int fontSize, Point position, Color color)
        {
            var fontDescription = Font.Description;
            fontDescription.Height = fontSize;

            var fontDimension = Font.MeasureText(null, text, new Rectangle(position.X, position.Y, 0, 0), FontDrawFlags.Center | FontDrawFlags.VerticalCenter);
            Font.DrawText(null, text, fontDimension, FontDrawFlags.Center | FontDrawFlags.VerticalCenter, color);
        }

        // Method for Drawing Lines
        internal static void DrawLine(float x1, float y1, float x2, float y2, float w, Color color)
        {
            DrawLineValue.Width = w;
            DrawLineValue.Antialias = false;
            DrawLineValue.GLLines = true;

            RawVector2[] vertices =
            {
                new RawVector2(x1, y1),
                new RawVector2(x2, y2),
            };

            DrawLineValue.Begin();
            DrawLineValue.Draw(vertices, new RawColorBGRA(color.B, color.G, color.R, color.A));
            DrawLineValue.End();
        }

        // Method for drawing Filled Boxes
        internal static void DrawFilledBox(float x, float y, float w, float h, Color color)
        {
            Vector2[] vLine = new Vector2[2];

            DrawFilledBoxLine.Width = w;
            DrawFilledBoxLine.GLLines = true;
            DrawFilledBoxLine.Antialias = false;

            vLine[0].X = x + (w / 2);
            vLine[0].Y = y;
            vLine[1].X = x + (w / 2);
            vLine[1].Y = y + h;

            DrawFilledBoxLine.Begin();
            DrawFilledBoxLine.Draw(vLine, new RawColorBGRA(color.B, color.G, color.R, color.A));
            DrawFilledBoxLine.End();

            /*              Example             */
            /* DrawFilledBox(x , y, w, h, color); */
        }

        // Method for Drawing Crosshair Boxes
        internal static void DrawCrosshairBox(float x, float y, float w, float h, float px, Color color)
        {
            DrawFilledBox(x, y + h, w, px, color);
            DrawFilledBox(x - px, y, px, h, color);
            DrawFilledBox(x, y - px, w, px, color);
            DrawFilledBox(x + w, y, px, h, color);

            /*              Example             */
            /* DrawCrosshairBox(x , y, w, h, px, color); */
        }

        // Method for drawing 2D Boxes
        internal static void DrawBox(float x, float y, float w, float h, Color color)
        {
            Vector2[] vertices =
            {
                new Vector2(x, y),
                new Vector2(x + w, y),
                new Vector2(x + w, y + h),
                new Vector2(x, y + h),
                new Vector2(x, y),
            };
            DrawBoxLine.Begin();
            DrawBoxLine.Draw(vertices, color);
            DrawBoxLine.End();

            /*              Example             */
            /* DrawBox(x , y, w, h, color); */
        }

        // Method for drawing a point on the screen
        internal static void DrawPoint(float x, float y, Color color)
        {
            DrawFilledBox(x, y, 1, 1, color);

